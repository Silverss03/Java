    private void SapxepNHbtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SapxepNHbtActionPerformed
        // TODO add your handling code here:
        if(SapxepNHbt.getSelectedIndex() == 1){
            lNganHang.sort(new Comparator<NganHang>(){
                @Override
                public int compare(NganHang o1, NganHang o2) {
                    String[] t1 = o1.getName().split("\\s+") ;
                    String[] t2 = o2.getName().split("\\s+") ;
                    String n1 = t1[t1.length - 1] + o1.getName() ;
                    String n2 = t2[t2.length - 1] + o2.getName() ;
                    return n1.compareTo(n2) ;
                } 
                
            });
        }
        if(SapxepNHbt.getSelectedIndex() == 2){
            lNganHang.sort(new Comparator<NganHang>(){
                @Override
                public int compare(NganHang o1, NganHang o2) {
                    return o1.getAmount() - o2.getAmount() ;
                } 
                
            });
        }
        tmNH.setRowCount(0);
        for(NganHang x : lNganHang){
            tmNH.addRow(x.toObject());
        }
    }//GEN-LAST:event_SapxepNHbtActionPerformed

