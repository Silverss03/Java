    public void loadData(){
        File f = new File(fDV) ; 
        if(f.exists()){
            lDV = IOFile.doc(fDV) ;
        }
        else{
            lDV = new ArrayList<>() ; 
        }
        f = new File(fNV) ; 
        if(f.exists()){
            lNV = IOFile.doc(fNV) ;
        }
        else{
            lNV = new ArrayList<>() ; 
        }        
    }

    public void show(List<DichVu> lDV, List<NhanVien> lNV){
        tmDV.setRowCount(0);
        for(DichVu x : lDV){
            tmDV.addRow(x.toObject());
        }
        tmNV.setRowCount(0);
        for(NhanVien x : lNV){
            tmNV.addRow(x.toObject());
        }
    }
