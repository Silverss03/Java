    private void cappnhatKHbtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cappnhatKHbtActionPerformed
        // TODO add your handling code here:
        if(them){
            String name, address, type;
            try{
                name = hotenKH.getText() ;
                address = diachiKH.getText() ;
                type = loaiKH.getSelectedItem().toString() ;
                KhachHang s = new KhachHang(name, address, type) ;
                tmKH.addRow(s.toObject());
                lKhachHang.add(s) ;
                nhapKHbt.setEnabled(true);
                cappnhatKHbt.setEnabled(false);
                them = false ;
            }
            catch(TrongException e){
                JOptionPane.showMessageDialog(this, "Phải điền đầy đủ các thông tin!");
                hotenKH.requestFocus();
            }
        }
        if(sua){
            int row = KHtb.getSelectedRow()  ;
            String name, address, type;
            int id ;
            try{
                id = Integer.valueOf(maKH.getText()) ;
                name = hotenKH.getText() ;
                address = diachiKH.getText() ;
                type = loaiKH.getSelectedItem().toString() ;
                KhachHang s = new KhachHang(name, address, type) ;
                lKhachHang.set(row - 1, s) ;
                tmKH.setValueAt(id, row, 0);
                tmKH.setValueAt(name, row, 1);
                tmKH.setValueAt(address, row, 2);
                tmKH.setValueAt(type, row, 3);
                SuaKHbt.setEnabled(true);
                cappnhatKHbt.setEnabled(false);
                sua = false ;
            }
            catch(TrongException e){
                JOptionPane.showMessageDialog(this, "Phải điền đầy đủ các thông tin!");
                hotenKH.requestFocus();
            }
        }
    }//GEN-LAST:event_cappnhatKHbtActionPerformed

