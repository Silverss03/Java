    private void btSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaActionPerformed
        // TODO add your handling code here:
        int r = jTable2.getSelectedRow() ;
        if(r >= 0 && r < jTable2.getRowCount()){
            String hoten = jTextField6.getText() ;
            String diachi = jTextField7.getText() ;
            String dienthoai = jTextField8.getText() ;
            tmNV.setValueAt(hoten, r, 1);
            tmNV.setValueAt(diachi, r, 2);
            tmNV.setValueAt(dienthoai, r, 3);
            int ma = Integer.parseInt(jTextField5.getText()) ;
            NhanVien nv = new NhanVien(ma, hoten, diachi, dienthoai) ;
            lNV.set(r, nv) ;
        }
        else{
            JOptionPane.showMessageDialog(this, "Chọn dịch vụ để xóa!");
            return ;
        }
    }//GEN-LAST:event_btSuaActionPerformed

