    public void loadMa(){
        MaKHcombobox.removeAllItems();
        MaNHCombobox.removeAllItems(); 
        loadKhachHang() ; 
        loadNganHang() ; 
        for(NganHang x : lNganHang){
            MaNHCombobox.addItem(x.getId() + "");
        }
        for(KhachHang x : lKhachHang){
            MaKHcombobox.addItem(x.getId() + "");
        }
    }
    
    public boolean maDaCo(int maKH, int maNH){
        for(SoTietKiem x : lSoTK){
            if(x.getKhachhang().getId() == maKH && x.getNganhang().getId() == maNH){
                return true ;
            }
        }
        return false ;
    }
