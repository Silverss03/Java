    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        // TODO add your handling code here:
        int n = lDV.size() ; 
        if(n > 0){
            DichVu.setId_n(lDV.get(n - 1).getId() + 1);
        }
        jTextField1.setText(DichVu.getId_n() + "");
        String ten, nhom ; 
        Double chiphi, cuoc ;
        try{
            ten = jTextField2.getText() ;
            nhom = jComboBox1.getSelectedItem().toString() ; 
            chiphi = Double.valueOf(jTextField3.getText()) ;
            cuoc = Double.valueOf(jTextField4.getText()) ;
            DichVu dv = new DichVu(ten, nhom, chiphi, cuoc) ; 
            tmDV.addRow(dv.toObject());
            lDV.add(dv) ;
        }
        catch(TrongException e){
            JOptionPane.showMessageDialog(this, "Không để trống!");
            return ;
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Không nhập chữ vào ô cần nhập số!");
            return ;            
        }
        
    }//GEN-LAST:event_btThemActionPerformed

